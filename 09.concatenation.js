let string1 = "Gourav";
let string2 = "Mani";

let mergedName = string1 + "-" + string2;
console.log(mergedName); //Gourav-Mani


string1 = "17";
string2 = "10";

mergedName = string1 + "" + string2;
console.log(mergedName); //1710

mergedName = +string1 + +string2;  //Converted to number
console.log(mergedName); //27


